let calculadora = {
    
    sumar:function (num1, num2) {
       return num1+num2
    },

    resta:function (num1, num2) {
        return num1-num2
     },


     divicion:function (num1, num2) {
        return num1/num2
     },


     multiplicacion:function (num1, num2) {
        return num1*num2
     }

}

export {calculadora}